// Uma estrura struct é uma coleção de campos.

// Um campo é simplesmente um valor de dado associado a uma estrutura
// de dados. Seu valor pode ser um tipo primitivo ou uma estrutura de dados.

// Sua definição é como um modelo para o compilador sobre como organizar os campos
// na memória próximos uns dos outros.

struct SeaCreature {
    animal_type: String,
    name: String,
    legs: i32,
    weapon: String,
}